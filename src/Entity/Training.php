<?php

namespace App\Entity;

use App\Repository\TrainingRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=TrainingRepository::class)
 */
class Training
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $title;

    /**
     * @ORM\Column(type="datetime")
     */
    private $face_date;

    /**
     * @ORM\Column(type="integer")
     */
    private $hours;

    /**
     * @ORM\OneToOne(targetEntity=Session::class, inversedBy="training", cascade={"persist", "remove"})
     */
    private $session;

    /**
     * @ORM\ManyToOne(targetEntity=Trainer::class, inversedBy="trainings")
     */
    private $trainer;

    /**
     * @ORM\OneToMany(targetEntity=Evaluation::class, mappedBy="training")
     */
    private $evaluations;

    /**
     * @ORM\OneToMany(targetEntity=Certificate::class, mappedBy="training")
     */
    private $certificates;

    public function __construct()
    {
        $this->evaluations = new ArrayCollection();
        $this->certificates = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getFaceDate(): ?\DateTimeInterface
    {
        return $this->face_date;
    }

    public function setFaceDate(\DateTimeInterface $face_date): self
    {
        $this->face_date = $face_date;

        return $this;
    }

    public function getHours(): ?int
    {
        return $this->hours;
    }

    public function setHours(int $hours): self
    {
        $this->hours = $hours;

        return $this;
    }

    public function getSession(): ?Session
    {
        return $this->session;
    }

    public function setSession(?Session $session): self
    {
        $this->session = $session;

        return $this;
    }

    public function getTrainer(): ?Trainer
    {
        return $this->trainer;
    }

    public function setTrainer(?Trainer $trainer): self
    {
        $this->trainer = $trainer;

        return $this;
    }

    /**
     * @return Collection|Evaluation[]
     */
    public function getEvaluations(): Collection
    {
        return $this->evaluations;
    }

    public function addEvaluation(Evaluation $evaluation): self
    {
        if (!$this->evaluations->contains($evaluation)) {
            $this->evaluations[] = $evaluation;
            $evaluation->setTraining($this);
        }

        return $this;
    }

    public function removeEvaluation(Evaluation $evaluation): self
    {
        if ($this->evaluations->contains($evaluation)) {
            $this->evaluations->removeElement($evaluation);
            // set the owning side to null (unless already changed)
            if ($evaluation->getTraining() === $this) {
                $evaluation->setTraining(null);
            }
        }

        return $this;
    }

    /**
     * @return Collection|Certificate[]
     */
    public function getCertificates(): Collection
    {
        return $this->certificates;
    }

    public function addCertificate(Certificate $certificate): self
    {
        if (!$this->certificates->contains($certificate)) {
            $this->certificates[] = $certificate;
            $certificate->setTraining($this);
        }

        return $this;
    }

    public function removeCertificate(Certificate $certificate): self
    {
        if ($this->certificates->contains($certificate)) {
            $this->certificates->removeElement($certificate);
            // set the owning side to null (unless already changed)
            if ($certificate->getTraining() === $this) {
                $certificate->setTraining(null);
            }
        }

        return $this;
    }

    public function __toString() {

        return $this->getTitle();
    }
}
